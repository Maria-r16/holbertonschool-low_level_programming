#include "main.h"

/**
 *print_alphabet_x10 - alphabet printing teen using a new main function.
 *Description: alphabet by teen times
 *Return: 0
 */

void print_alphabet_x10(void)
{
for (int i = 0 ; i = 9 ; i++)
{
	char alpha;

	for (alpha = 'a'; alpha <= 'z' ; alpha++)
	{
	_putchar(alpha);
	if (alpha == 'z')
	{
	_putchar('\n');
	}
	}
}
}
